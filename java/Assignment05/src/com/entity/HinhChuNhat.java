package com.entity;

public class HinhChuNhat {

	public int TinhChuVi(int a, int b) {
		System.out.println("Chu vi hinh vuong / chu nhat là : " + (a+b)*2);
		return (a+b)*2;
	}
	
	public int TinhDientich(int a, int b) {
		System.out.println("Dien tich hinh vuong / chu nhat là : " + a*b);
		return a*b;
	}
	
	
	
	
}



